package com.a1311;

public class RelationalOp 
{
	
	public static void main(String[] args) 
	{
		
		int a = 6;
		int b = 5;
		
		System.out.println(a<b);
		System.out.println(a>b);
		System.out.println(a<=b);
		System.out.println(a>=b);
		System.out.println(a==b);
		System.out.println(a!=b);
		
		
	}
	
}
